bool greater(int a, int b)
{
	int z = a > b;
	return z;
}

bool less(int a, int b)
{
	int z = a < b;
	return z;
}

bool greater_or_equal(int a, int b)
{
	int z = a >= b;
	return z;
}

bool less_or_equal(int a, int b)
{
	int z = a <= b;
	return z;
}

bool equal(int a, int b)
{
	int z = a == b;
	return z;
}

bool not_equal(int a, int b)
{
	int z = a != b;
	return z;
}
